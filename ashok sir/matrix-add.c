// matrix addition
#include <stdio.h>
void main()
{
    int a[100][100], b[100][100];
    int g, c;
    printf("Enter the size of a martix :");
    scanf("%dx%d", &g, &c);

    for (int i = 0; i <= g; i++)
    {
        for (int j = 0; j <= 0; j++)
        {
            prrintf("%d%d", a[g], a[c]);
        }
    }
    for (int i = 0; i <= g; i++)
    {
        for (int j = 0; j <= 0; j++)
        {
            prrintf("%d%d", b[g], b[c]);
        }
    }
}